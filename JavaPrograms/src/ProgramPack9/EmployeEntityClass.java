package ProgramPack9;

public class EmployeEntityClass {
	int id;
	String name;
	int age;
	int salary;
	int phone_No;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getSalary() {
		return salary;
	}

	public void setSalary(int salary) {
		this.salary = salary;
	}

	public int getPhone_No() {
		return phone_No;
	}

	public void setPhone_No(int phone_No) {
		this.phone_No = phone_No;
	}

}
